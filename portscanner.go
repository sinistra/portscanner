// ScanResult is a struct of Port & State
type ScanResult struct {
	Port  int
	State string
}

func main() {
	fmt.Println("Port Scanning")
	results := InitialScan("localhost")
	DisplayResults(results)
}

// ScanPort test if a host:port is open
func ScanPort(protocol, hostname string, port int) ScanResult {
	result := ScanResult{Port: port}
	address := hostname + ":" + strconv.Itoa(port)
	conn, err := net.DialTimeout(protocol, address, 60*time.Second)

	if err != nil {
		result.State = "Closed"
		fmt.Print(".")
		return result
	}
	defer conn.Close()
	result.State = "Open"
	fmt.Printf("(%d)", result.Port)
	return result
}

// InitialScan calls ScanPort for a range of ports
func InitialScan(hostname string) []ScanResult {

	var results []ScanResult

	for i := 0; i <= 65535; i++ {
		results = append(results, ScanPort("tcp", hostname, i))
	}
	fmt.Println()
	return results
}

// DisplayResults displays all open ports with a description
func DisplayResults(results []ScanResult) {
	portlist := PortList()
	for _, result := range results {
		if result.State == "Open" {
			fmt.Println(result.Port, portlist[result.Port])
		}
	}
}

